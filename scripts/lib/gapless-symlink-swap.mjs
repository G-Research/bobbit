#!/usr/bin/env node
/**
 * Zero-window variant of atomic-copy-dir.mjs's directory swap, using stable
 * symlink indirection instead of a rename-the-directory-itself swap.
 *
 * Background (PR #160 "Close tools-builtin Docker mount race in
 * build:server"): `atomicReplaceDir` (atomic-copy-dir.mjs) fixed the original
 * `rm -rf dist/server/defaults && copy(...)` race (25% -> 95% sandbox-spawn
 * reliability under churn) by building into a staging dir and swapping it
 * into place with TWO renames (old dest -> `.old-*`, staging -> dest). That
 * closed almost all of the window, but PR #160 documented an honest residual
 * risk: "a small gap remains, tied to Docker Desktop's virtiofs
 * directory-entry propagation across the two renames rather than to
 * application-level atomicity." It also prototyped, Docker-verified (50/50
 * under aggressive churn), and deliberately did NOT ship a fully gapless
 * design: "stable symlink indirection, swapped via a single rename()" —
 * deferred because `dist/` ships inside the published npm package and
 * cross-platform (Windows) symlink semantics needed separate verification.
 * This module is that design, shipped with the verification it was waiting
 * on wired in as an automatic, fail-open capability probe.
 *
 * Design: instead of `dest` being a real directory that gets swapped for a
 * new real directory, `dest` becomes a symlink into `${dest}.versions/`, and
 * each build:
 *   1. populates a freshly-named version dir `${dest}.versions/v-<ts>-<pid>`
 *      (never touches the currently-live version — no readers are affected);
 *   2. symlinks a temp link next to `dest` at that new version;
 *   3. `rename(2)`s the temp link onto `dest` in a SINGLE atomic operation.
 *
 * A concurrent reader resolving `dest` (including a `docker run -v
 * <dest>:...:ro` bind-mount source lookup) during step 3 sees either the
 * fully-old symlink target or the fully-new one — the underlying version
 * directories are never partially written from a reader's perspective,
 * because neither is ever mutated after it's linked in.
 *
 * Retention as a deliberate answer to the PR #160 residual risk: we do NOT
 * delete a version the moment `dest` stops pointing at it. Each swap keeps
 * the version it just superseded around for exactly one more generation,
 * only pruning versions older than that. If a guest's view of the bind mount
 * genuinely lags the host-side rename (the virtiofs propagation risk PR #160
 * called out), it still resolves through a fully-populated — merely stale —
 * tree, never a missing one, until the NEXT swap catches it up.
 *
 * WINDOWS / npm-publish fail-open contract:
 *   - Symlink creation needs Developer Mode or elevation on Windows, and the
 *     `dest.versions` + rename-the-symlink scheme depends on rename()
 *     semantics that some filesystems don't support for symlinks. Rather
 *     than special-case platforms, this module runs a functional capability
 *     probe (create two symlinks, rename one onto the other, read through
 *     both) the first time it's used per process and caches the boolean
 *     result. Any failure — permission denied, ENOSYS, EXDEV, anything —
 *     falls back to the existing `atomicReplaceDir` two-rename behavior
 *     unconditionally. Nothing about the public API changes when this
 *     happens; callers don't need to branch.
 *   - `dist/` is shipped verbatim inside the published npm tarball (see
 *     `files` in package.json) — built once on the maintainer's machine, not
 *     regenerated by `npm install` on the end user's machine. If the
 *     maintainer's machine supports symlinks (any non-Windows box), the
 *     published `dist/server/defaults` would itself be a symlink, and its
 *     extraction on a Windows *end user's* machine is a completely different
 *     capability check that this in-process probe cannot observe. To keep
 *     that risk out of the picture entirely, `npm run prepublishOnly`
 *     (scripts/prepublish-build.mjs) forces the fallback unconditionally via
 *     `BOBBIT_DIST_NO_SYMLINK=1`, so the tarball we publish never contains
 *     one of these symlinks regardless of what machine cut the release.
 */
import fs from "node:fs";
import path from "node:path";
import { atomicReplaceDir, copyDirRecursive } from "./atomic-copy-dir.mjs";

// undefined = not probed yet; true/false = cached probe result for this process.
let capabilityCache;

// Monotonic per-process disambiguator for version directory names — see its
// use in atomicReplaceDirGapless for why pid+timestamp alone isn't enough.
let versionCounter = 0;

/**
 * Functional probe: exercises exactly the operations the gapless swap relies
 * on (symlink creation + rename-the-symlink-onto-another-symlink), not just
 * "does fs.symlinkSync throw." A platform/filesystem that allows creating a
 * symlink but not atomically renaming over one would silently defeat the
 * whole point.
 */
function probeSymlinkCapability(nearDir) {
  if (process.env.BOBBIT_DIST_NO_SYMLINK === "1") return false;
  // Symlink targets resolve relative to the LINK's own directory, not the
  // process cwd — a relative target here would silently break resolution
  // once the link and its target are nested a directory apart from cwd (e.g.
  // when a caller passes a repo-relative dest like "dist/server/defaults").
  // Absolute paths throughout side-step that entirely.
  const absNearDir = path.resolve(nearDir);
  let probeRoot;
  try {
    fs.mkdirSync(absNearDir, { recursive: true });
    probeRoot = fs.mkdtempSync(path.join(absNearDir, ".gapless-probe-"));
    const targetA = path.join(probeRoot, "a");
    const targetB = path.join(probeRoot, "b");
    fs.mkdirSync(targetA);
    fs.writeFileSync(path.join(targetA, "marker"), "a");
    fs.mkdirSync(targetB);
    fs.writeFileSync(path.join(targetB, "marker"), "b");

    const link = path.join(probeRoot, "link");
    const link2 = path.join(probeRoot, "link2");
    fs.symlinkSync(targetA, link, "dir");
    if (fs.readFileSync(path.join(link, "marker"), "utf8") !== "a") return false;

    fs.symlinkSync(targetB, link2, "dir");
    fs.renameSync(link2, link); // the exact atomic op the real swap depends on
    if (!fs.lstatSync(link).isSymbolicLink()) return false;
    if (fs.readFileSync(path.join(link, "marker"), "utf8") !== "b") return false;

    return true;
  } catch {
    return false;
  } finally {
    if (probeRoot) fs.rmSync(probeRoot, { recursive: true, force: true });
  }
}

function supportsGaplessSymlinkSwap(nearDir) {
  if (capabilityCache === undefined) {
    capabilityCache = probeSymlinkCapability(nearDir);
  }
  return capabilityCache;
}

/** Test-only: force the next call to re-run the capability probe. */
export function _resetGaplessCapabilityCacheForTests() {
  capabilityCache = undefined;
}

/** Test-only: read the cached probe result without triggering a probe. */
export function _gaplessCapabilityCacheForTests() {
  return capabilityCache;
}

function sweepStrayDebris(dest) {
  const destParent = path.dirname(dest);
  const destBase = path.basename(dest);
  try {
    for (const entry of fs.readdirSync(destParent)) {
      if (entry.startsWith(`${destBase}.tmp-link-`) || entry.startsWith(`${destBase}.migrating-`)) {
        fs.rmSync(path.join(destParent, entry), { recursive: true, force: true });
      }
    }
  } catch {
    // destParent doesn't exist yet on a first-ever build — nothing to sweep.
  }
}

/**
 * @param {string} src source directory to copy from; a no-op if it doesn't exist
 * @param {string} dest destination path to atomically (re)point at fresh content
 * @param {{ skipDirs?: Set<string>, populate?: (staging: string) => void, retentionWindowMs?: number }} [opts]
 *   `skipDirs`/`populate` share the same contract as `atomicReplaceDir`.
 *   `retentionWindowMs` (default 5000) is the extra grace period, on top of
 *   always keeping the current + immediately-previous version, that an
 *   old version is kept around before pruning — see the prune step below for
 *   why. Lower it (e.g. to 0) only in tests that want deterministic pruning
 *   of anything older than current+previous.
 */
export function atomicReplaceDirGapless(src, dest, opts = {}) {
  // Resolve to absolute up front: the swap creates symlinks whose targets
  // must be absolute (see probeSymlinkCapability's comment) so resolution
  // doesn't depend on where `dest` sits relative to the process cwd. Callers
  // (copy-defaults.mjs, copy-builtin-packs.mjs) pass repo-relative paths.
  dest = path.resolve(dest);
  const destParent = path.dirname(dest);
  fs.mkdirSync(destParent, { recursive: true });

  if (!supportsGaplessSymlinkSwap(destParent)) {
    // Fail open: identical external behavior to before this module existed.
    atomicReplaceDir(src, dest, opts);
    // If a prior process DID have symlink capability and left `dest` pointing
    // into a `.versions/` dir, atomicReplaceDir above has already replaced
    // `dest` itself with a real directory (renaming the old symlink out of
    // the way and discarding it) — the versioned dirs it used to point at
    // are now orphaned. Reclaim them; nothing can still be resolving through
    // the symlink because it no longer exists at `dest`.
    fs.rmSync(`${dest}.versions`, { recursive: true, force: true });
    return;
  }

  if (!opts.populate && !fs.existsSync(src)) return;

  sweepStrayDebris(dest);

  const versionsDir = `${dest}.versions`;
  fs.mkdirSync(versionsDir, { recursive: true });

  const pid = process.pid;
  const ts = Date.now();
  // `versionCounter` guards against two swaps landing in the same
  // millisecond of the same process (e.g. rapid successive rebuilds, or
  // tests) — pid+timestamp alone could otherwise collide and cause the
  // second swap to overwrite files inside a version a reader may already be
  // resolving through, defeating the whole point of never mutating a linked
  // version in place.
  const versionName = `v-${ts}-${pid}-${versionCounter++}`;
  const versionDir = path.join(versionsDir, versionName);

  if (opts.populate) {
    fs.mkdirSync(versionDir, { recursive: true });
    opts.populate(versionDir);
  } else {
    copyDirRecursive(src, versionDir, opts.skipDirs);
  }

  const tempLink = `${dest}.tmp-link-${pid}-${ts}`;
  fs.symlinkSync(versionDir, tempLink, "dir");

  let previousVersionName;
  let destStat;
  try {
    destStat = fs.lstatSync(dest);
  } catch {
    destStat = null;
  }

  if (!destStat) {
    // First-ever build for this dest: nothing to replace, gapless from t=0.
    fs.renameSync(tempLink, dest);
  } else if (destStat.isSymbolicLink()) {
    // Steady state. Remember what `dest` currently points at so it survives
    // this swap as the retained "previous" generation (see file header),
    // then flip the symlink with a single rename — the zero-window swap.
    try {
      previousVersionName = path.basename(fs.readlinkSync(dest));
    } catch {
      previousVersionName = undefined;
    }
    fs.renameSync(tempLink, dest);
  } else {
    // One-time migration off the pre-existing real-directory scheme (a build
    // from before this change, or a prior run where the capability probe
    // failed and used the atomicReplaceDir fallback). POSIX rename() cannot
    // atomically replace a real directory with a symlink — the "must both be
    // directories, or neither" rule — so this ONE transition falls back to
    // the same two-rename swap atomic-copy-dir has always used. Every build
    // after this one is a true single-rename gapless swap.
    const migratingOld = `${dest}.migrating-${pid}-${ts}`;
    fs.renameSync(dest, migratingOld);
    fs.renameSync(tempLink, dest);
    fs.rmSync(migratingOld, { recursive: true, force: true });
  }

  // Prune old versions, but keep two independent safety nets:
  //   1. The current version and the one it just superseded are ALWAYS kept
  //      (the minimum needed for the virtiofs-lag margin described above).
  //   2. Anything younger than `retentionWindowMs` is ALSO kept, regardless
  //      of (1) — under a rapid-fire rebuild burst (e.g. a save-triggered
  //      restart loop, or this module's own test suite), many more than two
  //      versions can be "in flight" at once: a `docker run` bind-mount can
  //      take hundreds of ms to actually resolve its source path, which can
  //      span many swaps if they're firing every few ms. Retaining only the
  //      last two by count let an in-progress container's target get pruned
  //      out from under it before the mount ever resolved — reproduced
  //      locally (real `docker run` against a live churn loop): a fixed
  //      keep-2 policy saw ~90% empty mounts under aggressive churn, which
  //      this time-based grace period (default 5s, generous relative to
  //      normal container-create latency) eliminated in the same repro.
  const retentionWindowMs = opts.retentionWindowMs ?? 5000;
  const now = Date.now();
  for (const entry of fs.readdirSync(versionsDir)) {
    if (entry === versionName || entry === previousVersionName) continue;
    const entryTs = Number(/^v-(\d+)-/.exec(entry)?.[1] ?? 0);
    if (now - entryTs < retentionWindowMs) continue;
    fs.rmSync(path.join(versionsDir, entry), { recursive: true, force: true });
  }
}
